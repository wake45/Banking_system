package Project._Percent_Project.controller;

public class TransactionsHistoryInquiryForm {
    private String accountNumber;
    private String inquiryStartDate;
    private String inquiryEndDate;
    private String inquiryType;
    private String inquirySort;
    private String id;

    public String getAccountNumber() {
        return accountNumber;
    }

    public void setAccountNumber(String accountNumber) {
        this.accountNumber = accountNumber;
    }

    public String getInquiryStartDate() {
        return inquiryStartDate;
    }

    public void setInquiryStartDate(String inquiryStartDate) {
        this.inquiryStartDate = inquiryStartDate;
    }

    public String getInquiryEndDate() {
        return inquiryEndDate;
    }

    public void setInquiryEndDate(String inquiryEndDate) {
        this.inquiryEndDate = inquiryEndDate;
    }

    public String getInquiryType() {
        return inquiryType;
    }

    public void setInquiryType(String inquiryType) {
        this.inquiryType = inquiryType;
    }

    public String getInquirySort() {
        return inquirySort;
    }

    public void setInquirySort(String inquirySort) {
        this.inquirySort = inquirySort;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
